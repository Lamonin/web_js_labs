
let n = prompt("Введите количество точек:");

while (n === "" || isNaN(n) || n <= 0) {
    n = prompt("Некорректный ввод. Введите количество точек:");
}

let q1 = 0, q2 = 0, q3 = 0, q4 = 0;

for (let i = 0; i < n; i++) {
    const x = Math.floor(Math.random() * 21) - 10;
    const y = Math.floor(Math.random() * 21) - 10;

    if (x > 0 && y > 0) {
        q1++;
    } else if (x < 0 && y > 0) {
        q2++;
    } else if (x < 0 && y < 0) {
        q3++;
    } else if (x > 0 && y < 0) {
        q4++;
    }
}

const result = `Количество точек в первой четверти: ${q1}\n` +
    `Количество точек во второй четверти: ${q2}\n` +
    `Количество точек в третьей четверти: ${q3}\n` +
    `Количество точек в четвёртой четверти: ${q4}`;

alert(result);
